# Μετατροπή μισθού (40 ωρών εβδομαδιαίας εργασίας) σε ωρομίσθιο και σε ημερομίσθιο.

wage = float(input("Δώστε ποσό μισθού: "))
hourly_wage = wage * 0.006
print ("Το ωρομίσθιο είναι", hourly_wage, "ευρώ")
daily_wage_8 = wage * 0.048
print ("Το ημερομίσθιο 8 ωρών είναι", daily_wage_8, "ευρώ")
daily_wage_6_40 = wage / 25
print ("Το ημερομίσθιο 6 ωρών και 40 λεπτών είναι", daily_wage_6_40, "ευρώ")
