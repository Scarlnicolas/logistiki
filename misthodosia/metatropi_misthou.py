# Μετατροπή μισθού (40 ωρών εβδομαδιαίας εργασίας) σε ωρομίσθιο και σε ημερομίσθιο.

mist = float(input("Δώστε ποσό μισθού: "))
oro = mist * 0.006
print ("Το ωρομίσθιο είναι", oro, "ευρώ")
ime_8 = mist * 0.048
print ("Το ημερομίσθιο 8 ωρών είναι", ime_8, "ευρώ")
ime_6_40 = mist / 25
print ("Το ημερομίσθιο 6 ωρών και 40 λεπτών είναι", ime_6_40, "ευρώ")
